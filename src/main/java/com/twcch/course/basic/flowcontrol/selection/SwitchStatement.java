package com.twcch.course.basic.flowcontrol.selection;

public class SwitchStatement {

	public static void main(String[] args) {

		/* 
		 * integer: byte, short, char, int
		 * String
		 * Enum
		 */
		int a = 1;

		switch (a) {
		case 1:
			System.out.println("1");
			break;
		case 2:
			System.out.println("2");
			break;
		default:
			break;
		}

	}

}
