package com.twcch.course.basic.operatorandoperand;

public class RelationalOperator {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 3;
		
		// 關係運算子
		System.out.printf("a > b: %b\n", a > b);
		System.out.printf("a < b: %b\n", a < b);
		System.out.printf("a >= b: %b\n", a >= b);
		System.out.printf("a <= b: %b\n", a <= b);
		System.out.printf("a == b: %b\n", a == b);
		System.out.printf("a != b: %b\n", a != b);

	}

}
