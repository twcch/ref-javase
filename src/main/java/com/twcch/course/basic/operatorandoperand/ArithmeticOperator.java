package com.twcch.course.basic.operatorandoperand;

public class ArithmeticOperator {

	public static void main(String[] args) {

		int a = 10;
		int b = 3;

		// arithmetic operator
		System.out.printf("a + b = %d\n", a + b);
		System.out.printf("a - b = %d\n", a - b);
		System.out.printf("a * b = %d\n", a * b);
		System.out.printf("a / b = %.2f\n", (double) a / b);

	}

}
