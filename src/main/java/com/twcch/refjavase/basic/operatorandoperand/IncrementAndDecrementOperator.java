package com.twcch.refjavase.basic.operatorandoperand;

public class IncrementAndDecrementOperator {

	public static void main(String[] args) {

		int a = 10;

		// increment and decrement operator
		System.out.printf("a++ = %d\n", a++);
		System.out.printf("a-- = %d\n", a--);
		System.out.printf("++a = %d\n", ++a);
		System.out.printf("--a = %d\n", --a);

	}

}
